int A[maxn], fa[maxn], sum[maxn];
bool v[maxn];

ll dfs(int i)
{
	if(v[i]) return 0;
	v[i] = true;
	if(! fa[i])
	{
		sum[i] -= A[i]; //clear the initial point
		return abs(sum[i]);
	}
	ll x = dfs(fa[i]); //goto the father place to clear the point 
	A[i] += sum[fa[i]]; //clear the tag of this point
	x += abs(A[i]);
	sum[i] += sum[fa[i]] - A[i]; //clear the tag of now
	return x;
}

